public class Question8 {
  public static void main(String[] args) {
    int a = 56;
    int b = 23;
    int add = a + b;
    int diff = a - b;
    int divQ = a / b;
    int divR = a % b;
    int product = a * b;

    System.out.println(a + " + " + b + " = " + add);
    System.out.println(a + " - " + b + " = " + diff);
    System.out.println(a + " / " + b + " = " + divQ);
    System.out.println(a + " % " + b + " = " + divR);
    System.out.println(a + " * " + b + " = " + product);
  }
}
