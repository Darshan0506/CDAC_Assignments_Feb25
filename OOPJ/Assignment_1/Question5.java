public class Question5 {
  public static void main(String[] args) {
    int a = 0b1010;
    System.out.println("Decimal of binary 1010: " + a);
    int b = 026;
    System.out.println("Decimal of Octal 26: " + b);
    int c = 0x1F;
    System.out.println("Decimal of hexadecimal 1F: " + c);
    float fl = 0.0f;
    System.out.println("Float literal: " + fl);
    double du = 523.69d;
    System.out.println("Double literal: " + du);
  }
}
