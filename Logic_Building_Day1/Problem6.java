class Problem6 {
  public static void main(String[] args) {
    int a = 125;
    int b = 24;
    int res1 = a + b;
    int res2 = a - b;
    int res3 = a * b;
    int res4 = a / b;

    System.out.println("125 + 24 = " + res1);
    System.out.println("125 - 24 = " + res2);
    System.out.println("125 x 24 = " + res3);
    System.out.println("125 / 24 = " + res4);
    System.out.println("125 mod 24 = " + res4);
  }
}
